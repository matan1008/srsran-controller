def test_parsing_gsm_rp_ack(tmp_path):
    p = tmp_path / 'gsm_rp_ack.pcap'
    p.write_bytes(bytes.fromhex(GSM_RP_ACK_PCAP_DATA))
    with FileCapture(str(p), use_json=True) as pcap:
        rp_ack = list(EventsFactory().from_packet(list(pcap)[0]))[0]
    assert rp_ack == {
        'event': GSM_RP_ACK_NAME,
        'rnti': 70,
        'time': datetime.datetime(2022, 3, 28, 8, 6, 46, 20625),
        'data': {
            'message_reference': 1
        },
        'enb_ip': '192.168.52.2',
    }
